package ntua.hci.scolio_kids.ui.login;

public class PatientFileMember {
    private String Name;
    private String Age;
    private String Height;
    private String Question;

    public PatientFileMember(){}

    public String getAttr1() {
        return Name;
    }

    public void setAttr1(String user1) {
        Name = user1;
    }

    public String getAttr2() {
        return Age;
    }

    public void setAttr2(String user2) {
        Age = user2;
    }

    public String getAttr3() {
        return Height;
    }

    public void setAttr3(String user3) {
        Height = user3;
    }

    public String getAttr4() {
        return Question;
    }

    public void setAttr4(String user4) {
        Question = user4;
    }

}
