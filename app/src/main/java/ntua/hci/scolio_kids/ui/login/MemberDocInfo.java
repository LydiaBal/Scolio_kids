package ntua.hci.scolio_kids.ui.login;

public class MemberDocInfo {
    private String name;
    private String city;
    private String address;

    public MemberDocInfo(){}

    public String getName() {
        return name;
    }

    public void setName(String myname) {
        name = myname;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String myage) {
        city = myage;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String myheight) {
        address = myheight;
    }

}
